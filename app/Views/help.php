        <h1>Help</h1>
        <p class="intro">
            Thank you for using OpenTexts.World! The site is currently in beta, which means we’re working on improving things continually. Please use the feedback tab at the bottom of the page to share thoughts, comments, or questions.
        </p>

        <h2>Finding a book</h2>
        <p>The easiest way to get started is to enter a few words into the search box. Not sure what to search for? Here’s a few suggestions:
        </p>
        <ul>
            <li><a href="/search?q=cheesecake">Cheesecake</a></li>
            <li><a href="/search?q=Auckland">Auckland</a></li>
            <li><a href="/search?q=chief+medical+report">Chief Medical Report</a></li>
        </ul>
        <p>OpenTexts.World is a search engine collating the digitised records of many different libraries worldwide. Clicking a record's title will take you to a third-party site for that particular record. If you want to return to your search, use your browser’s back button to return to OpenTexts.</p>
        
        <h2>Filtering results</h2>
        <p>Right now, you can filter search results by library and language. We’re working on adding more filters, so please share your feedback if there’s a filter you’d like to see.</p>
        <p>Filters will only be displayed if there are results available for that particular filter. You can select multiple filters at once by clicking each individual filter. Selecting "All libraries" (for example) will reset the filter to show results from all libraries.</p>
        
        <h2>Advanced search options</h2>
        <h3>Search for multiple words</h3>
        <p>
            Put "AND" between each search query. For example: <a href="/search?q=fruit+AND+juice">fruit AND juice</a>
        </p>
        
        <h3>Search for an exact phrase</h3>
        <p>
            Put the phrase inside double quotes. For example: <a href='/search?q="gutenberg+bible"'>"Gutenberg Bible"</a>
        </p>
        
        <h3>Excluding words</h3>
        <p>
            Put a - in front of the word you want to leave out.
            For example: <a href="/search?q=fruit+-juice">fruit -juice</a>
        </p>
        
        <h2>File formats</h2>
        <p>
            Each record's title will link to a third-party site controlled by the library that owns the title. 
            For some titles, direct file downloads are also available. These include:
        </p>
        <ul>
            <li>PDF</li>
            <li>Plain text</li>
            <li><a href="https://en.wikipedia.org/wiki/ALTO_(XML)">ALTO XML</a> extracted text</li>
            <li><a href="https://iiif.io/">IIIF manifest</a></li>
        </ul>
        <p>When available, links to direct downloads will be available at the bottom of each record.</p>
        
        <h2>Exporting results</h2>
        <p>
            You can export your search results in bulk by using the <a href="/search/export/?q=cheesecake">download full results</a>
            link at the bottom of each page. This will download the results in a <a href="https://en.wikipedia.org/wiki/Comma-separated_values">CSV format</a> which can be opened by any spreadsheet application.
        </p>
        <p>
            At present, only the first 5,000 results will be saved.  If you require more, please get
            in touch using the feedback tab below.
        </p>
        
