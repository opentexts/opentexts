        <h1>Help</h1>
        <p class="intro">
            Thank you for using OpenTexts.World! We have designed it to be as easy as possible to use
            and the following instructions will help you make the most of the site. If you have any comments
            or suggestions about how we can make it better, or questions about the site, please use the 
            feedback tab at the bottom of the page!
        </p>

        <h2>Basic searches</h2>
        <p>
            The easiest way to get started is to enter a few words into the search box. 
        </p>
        <p>
            <img src="/images/help/basic-search.png" alt="example search entered into the search box" />
        </p>
        <p>
            For example you could try the following searches:
        </p>
        <ul class="list-disc list-inside mb-2">
            <li><a href="/search?q=cheesecake">Cheesecake</a></li>
            <li><a href="/search?q=Auckland">Auckland</a></li>
            <li><a href="/search?q=chief+medical+report">Chief Medical Report</a></li>
        </ul>
        <p>
            Results will be shown for items that match your search. The words may appear in the title,
            the author's name, or other fields such as publisher, year, or location.
        </p>
        <p>
            When you perform a search, the first ten results will be shown. If there are more results, you can load these
            by pressing the 'More results' button.
        </p>
        
        <h2>Filtering the results</h2>
        <p>
            Once you have performed a search, it is possible to filter the results to a
            particular library (or libraries), or to a particular language (or languages). Depending on the
            search results, these options will differ.  For example if your search returns results
            from five different libraries, these will be listed, along with how many results there are for 
            each library.
        </p>
        <p>
            <img src="/images/help/library-filter.png" alt="An example of the Library filter showing results from five libraries" />
        </p>
        <p>
            Clicking on a library will then only show results from that library. If you click on the filter
            again, you can select additional libraries to add. Alternatively, selecting 'All libraries' will
            reset the filter to show results from all libraries.
        </p>
        <p>
            The languages filter works in the same way to allow you see items written in a particular language
            or languages.
        </p>
        
        <h2>Advanced options</h2>
        <h3>Searching for multiple words</h3>
        <p>
            Normally the results will show any item that contains one or more of the words that 
            you search for.  For example is you search for <a href="/search?q=fruit_juice">Fruit Juice</a> then all of the
            results will contain either Fruit, Juice, or both of these words.
        </p>
        <p>
            If you want to only see results that contain both words, search for 
            <a href="/search?q=fruit+AND+juice">Fruit AND Juice</a> instead.
        </p>
        
        <h3>Searching for phrases</h3>
        <p>
            If you want to search for a set phrase with each word being next to each other, put double quotes around
            the words.  For example <a href="/search?q=\"gutenberg+bible\"">"Gutenberg Bible"</a>.
        </p>
        
        <h3>Removing words</h3>
        <p>
            It is possible to remove words from searches by putting a minus sign '-' before the word.
            For example if you want to see all books about fruit but which don't mention juice, then you
            can search for <a href="/search?q=fruit+-juice">Fruit -Juice</a>.
        </p>
        
        <h2>File formats</h2>
        <p>
            Sometimes you will see different icons next to search results.  This means that the item can be viewed
            in certain formats. These include:
        </p>
        <ul class="list-disc list-inside mb-2">
            <li><img src="/images/pdf.png" width="16px" alt="PDF icon" />PDF</li>
            <li><img src="/images/txt.png" width="16px" alt="TXT icon" />Plain text</li>
            <li><img src="/images/alto-xml.png" width="16px" alt="ALTO XML icon" /><a href="https://en.wikipedia.org/wiki/ALTO_(XML)">ALTO XML</a> extracted text</li>
            <li><img src="/images/logo-iiif-34x30.png" width="16px" alt="PDF icon" /><a href="https://iiif.io/">IIIF manifest</a></li>
        </ul>
        
        <h2>Exporting results</h2>
        <p>
            If you wish to save the search results, for example so you can bulk download the items
            and perform research with them, you can use the <a href="/search/export/?q=cheesecake">Download full results</a>
            link. This will download the results in <a href="https://en.wikipedia.org/wiki/Comma-separated_values">CSV format</a> which can be opened in applications
            such as <a href="https://www.microsoft.com/en-gb/microsoft-365/excel">Microsoft Excel</a> 
            or <a href="https://www.openoffice.org/">Open Office</a>.
        </p>
        <p>
            At present, only the first 5,000 results will be saved.  If you require more, please get
            in touch using the feedback tab below.
        </p>
        
