    <h1 class="mt-5">About</h1>
    <p class="lead">OpenTexts.World is an experimental service developed to provide free access to the digitized text collections of UK libraries. Initiated during the COVID-19 lock down, it builds on the work of the <a href="https://gddnetwork.arts.gla.ac.uk/index.php/about-us/" title="link to GDD Network">GDD Network</a>.</p>

<h2>The problem</h2>
<p>Want to find a particular digitized book for your research,  homework or just to read? Well, you'll need to search the collections of many libraries. First, find where the library stores information about its digitized collections on its website, then search for the book you want.</p>

<p>Can't find the book at that library? Then you need to move onto the collection of the next library and repeat.</p>

<p>Repeat.</p>

<p>REPEAT!</p>

<h2>The opportunity</h2>

<p>During the COVID-19 pandemic of 2020, staff at National Library of Scotland learned that school teachers were trying to source digital texts to help support their students during lock down. Understanding the challenge that teachers were facing, staff in the Library's Digital Department decided to develop <a href="https://OpenTexts.World/" title="OpenTexts.World service">OpenTexts.World</a>.</p>

<h2>The service</h2>
<p>OpenTexts.World brings together, in a single search, the digitized collections of several <a href="/sources" title="contributors">major UK libraries</a>. A simple search interface, retrieves brief information about digitized books and other texts, and provides users with links to  resources.</p>

<p>The service is based on research undertaken by the Global Digitized Data Network, an Anglo-American research collaboration, funded by the <a href="https://ahrc.ukri.org/" title="AHRC">Arts and Humanities Research Council</a>.</p>

<h2>The data</h2>
<p>Metadata from contributing libraries is cleansed and processed into a common format. The data is then integrated into the <a href="https://en.wikipedia.org/wiki/Apache_Solr" title="info about Apache Solr">Solr search platform</a> and made discoverable via the OpenTexts.World service.</p>

<p>Metadata may be in <a href="https://www.loc.gov/marc/bibliographic/" title="MARC21 at Library of Congress, USA">MARC21</a> or <a href="/contribute" title="TSV information">TSV</a> (tab separated) format. Tools for processing other data formats are in development.</p>
