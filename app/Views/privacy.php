        <h2>What type of information we have</h2> 
        <p class="mb-2">
            The personal data we collect from you includes:
        </p>
        <ul>
            <li>questions, queries or feedback you leave, including your email address if you contact us</li>
            <li>information on how you use the site, using cookies and page tagging techniques</li>
            <li>anonymised website usage data via Google Analytics</li>
        </ul>

        <p class="mb-2">
            Google Analytics processes anonymised information about:
        </p>
        <ul>
            <li>the pages you visit on this site</li>
            <li>how long you spend on each web page</li>
            <li>how you got to the site</li>
            <li>what you click on while you’re visiting the site</li>
        </ul>
        <p>
            We do not store your personal information through Google Analytics (for example your name or address).
            We will not identify you through analytics information, and we will not combine analytics information 
            with other data sets in a way that would identify who you are.
        </p>

        <h2>Why we need your data</h2>
        <p>
            We collect information through Google Analytics to see how you use the site.
        </p>
        <p class="mb-2">
            We do this to help:
        </p>
        <ul>
            <li>make sure the site is meeting the needs of its users</li>
            <li>make improvements, for example improving site search</li>
        </ul>
        <p class="mb-2">
            We also collect data in order to:
        </p>
        <ul>
            <li>gather feedback to improve our services</li>
            <li>respond to any feedback you send us, if you’ve asked us to</li>
        </ul>
        
        <h2>Our legal basis for processing your data</h2>
        <p>
            The legal basis for processing personal data in relation to site security 
            is our legitimate interests, and the legitimate interests of our users, 
            in ensuring the security and integrity of the site.
        </p>
        <p>
            The legal basis for processing anonymised data for Google Analytics is your consent.
        </p>
        
        <h2>What we do with your data</h2>
        <p>
            The data we collect may be shared with other project partners or data contributors 
            so they can see how their data is being used. 
        </p>
        <p class="mb-2">
            We will not:
        </p>
            <ul>
                <li>sell or rent your data to third parties</li>
                <li>share your data with third parties for marketing purposes</li>
            </ul>
        <p>
            We will share your data if we are required to do so by law - for example, 
            by court order, or to prevent fraud or other crime.
        </p>
        
        <h2>How long we keep your data</h2>
        <p class="mb-2">
            We will only retain your personal data for as long as:
        </p>
        <ul>
            <li>it is needed for the purposes set out in this document</li>
            <li>the law requires us to</li>
        </ul>
        
        <h2>Where your data is processed and stored</h2>
        <p>
            We design, build and run our systems to make sure that your data is 
            as safe as possible at all stages, both while it’s processed and when it’s stored.
        </p>
        <p>
            All personal data is stored in the European Economic Area (EEA). 
            Data collected by Google Analytics and Doorbell.io may be transferred 
            outside the EEA for processing.
        </p>
        
        <h2>Your rights</h2>
        <p class="mb-2">
            You have the right to request:
        </p>
        <ul>
            <li>information about how your personal data is processed</li>
            <li>a copy of that personal data</li>
            <li>that anything inaccurate in your personal data is corrected immediately</li>
        </ul>
        <p class="mb-2">
            You can also:
        </p>
        <ul>
            <li>raise an objection about how your personal data is processed</li>
            <li>request that your personal data is erased if there is no longer a justification for it</li>
            <li>ask that the processing of your personal data is restricted in certain circumstances</li>
        </ul>
        <p>
            If you have any of these requests, please <a href="mailto:info@opentexts.world">get in contact</a> with us.
